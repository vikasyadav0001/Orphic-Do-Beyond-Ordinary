# cognition/
# Cognitive reasoning patterns — HOW agents think, not what they do.
# Contains reusable thinking strategies that can be plugged into any agent.
# Responsibilities:
#   - planning/    → Goal decomposition, task planning, step sequencing (e.g., ReAct, ToT)
#   - reasoning/   → Chain-of-thought, self-reflection, critique-and-refine loops
# Think of this as the "reasoning library" that agents import and use.
# Agents live in agents/ — this module defines the cognitive strategies they employ.
